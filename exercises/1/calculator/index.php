		<h2>Övning 2 - En enkel miniräknare</h2>
        <h3>Syfte</h3>
        <p>I denna övning ska ni fortsätta att arbeta med event listeners. Detta genom att bygga en enklare miniräknare som ska klara räknesätten addition, subtraktion, multiplikation och division.</p>
        <h3>Förkunskaper</h3>
        <p>Innan ni börjar med denna uppgiften bör ni läst/sett följande material:</p>
        <ul>
			<li><a href="lectures.php?type=Modul1&nr=Intro">Introduktion till event listeners [Video]</a></li>
            <li><a href="http://dvwebb.mah.se/tsanti/HT2012/DA122A/Material/JS/8.php" target="_blank">Översikt av event listeners [Text]</a></li>
            <li><a href="http://dvwebb.mah.se/tsanti/HT2012/DA122A/Material/JS/5.php">Repetition av pekaren "this"</a></li>
        </ul>
        <h3>Uppgiftsbeskrivning</h3>        
        <p>Ni ska alltså bygga en enkel miniräknare genom HTML/CSS/JavaScript där all händelsehantering ska ske genom event listeners. Exempel på hur er miniräknare skulle kunna se ut:</p>
        <img class="preview" src="exercises/1/calculator/calc.png" alt="Calculator">
        <p>Alltså bör er miniräknare bestå av tre text-fält (två för talen man vill ränka med och ett där resultatet visas) och fyra knappar (en för varje räknesätt). När man sedan klickar på en av knapparna ska resultatet av de två talen (med valt räknesätt) visas i resultat-rutan.</p>
        <p>Överkurs är att validera så att användaren verkligen skriver in siffror (och inte t.ex. bokstäver) innan de olika beräkningarna görs.</p>
        <h3>Startfiler</h3>
        <p>Ni får själva skapa era startfiler, lämpligen en HTML, en CSS och en JavaScript-fil. Tänk på att alla JavaScript-kod ska ligga i JavaScript-filen (alltså ingen JavaScript-kod i HTML-filen).</p>
